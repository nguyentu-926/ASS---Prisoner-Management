// Method to handle the addition of information about a person visiting for adoption
private void addPersonForAdoption() {
    // Retrieve the information from the text fields
    String fullName = jTextField1.getText();
    String address = jTextField2.getText();
    String phoneNumber = jTextField3.getText();
    String relationship = jTextField4.getText();
    String registrationDate = jTextField5.getText();
    String visitDate = jTextField6.getText();
    String prisonerName = jTextField7.getText();
    String prisonerId = jTextField8.getText();

    // Validate that all required fields are filled
    if (fullName.isEmpty() || address.isEmpty() || phoneNumber.isEmpty() || relationship.isEmpty() ||
            registrationDate.isEmpty() || visitDate.isEmpty() || prisonerName.isEmpty() || prisonerId.isEmpty()) {
        // Display an error message or handle the validation error as needed
        JOptionPane.showMessageDialog(this, "Yêu cầu nhập đầy đủ thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return; // Do not proceed with adding to the table if validation fails
    }

    // Add the information to the table
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.addRow(new Object[]{fullName, address, phoneNumber, relationship, registrationDate, visitDate, prisonerName, prisonerId});

    // Optionally, you can clear the input fields after adding the information
    clearInputFields();

}

