    private void confirmAndDeleteSelectedRow() {
    // Get the selected row index
    int selectedRowIndex = jTable1.getSelectedRow();

    // Check if a row is selected
    if (selectedRowIndex != -1) {
        // Display a confirmation dialog
        int choice = JOptionPane.showConfirmDialog(
                this,
                "Bạn có chắc chắn muốn xóa?",
                "Xác nhận xóa",
                JOptionPane.YES_NO_OPTION);

        // Check user's choice
        if (choice == JOptionPane.YES_OPTION) {
            // Remove the selected row from the table
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.removeRow(selectedRowIndex);

            // Optionally, you can perform additional actions such as updating your data source or database
        }
    } else {
        // If no row is selected, display a message to inform the user
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa.", "Đã chọn hàng để xóa", JOptionPane.WARNING_MESSAGE);
    }
}                           
    
